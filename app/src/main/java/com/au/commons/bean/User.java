package com.au.commons.bean;

/**
 * 用户是实体类
 * 
 * @author wxl
 */
public class User {

	private int uid;

	private String name;

	public int getUid() {
		return uid;
	}

	public void setUid(int uid) {
		this.uid = uid;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
